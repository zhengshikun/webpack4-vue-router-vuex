<template>
  <div>
    我是index{{count}}
    <button @click="handleIncrement">+1</button>
    <button @click="handleDecrease">-1</button>
  </div>
</template>

<script>
export default {
  computed: {
    count () {
      return this.$store.state.count
    }
  },
  methods: {
    handleIncrement () {
      this.$store.commit('increment', 1)
    },
    handleDecrease () {
      this.$store.commit({
        type: 'decrease',
        count:1
      })
    }
  }
}
</script>
